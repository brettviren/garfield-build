     /2.36,1.92,1.40,0.54,0.23,0.07,0.02,0.00/
      DATA XION/10.0,10.8,13.3,18.3,19.3,20.3,23.3,28.3,33.3,38.3,
     /43.3,48.3,53.3,58.3,68.3,78.3,88.3,98.3,120.,140.,
