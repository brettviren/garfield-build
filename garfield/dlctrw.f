     -           ' exists already; member will not be written.'
            RETURN
       ENDIF
*   Print some debugging output if requested.
       IF(LDEBUG)THEN
            PRINT *,' ++++++ DLCTRW DEBUG   : File= ',FILE(1:NCFILE),
     -           ', member= ',MEMBER(1:NCMEMB)
            PRINT *,'                         Remark= ',REMARK(1:NCREM)
       ENDIF
*** Open the dataset for sequential write and inform DSNLOG.
       CALL DSNOPN(FILE,NCFILE,12,'WRITE-LIBRARY',IFAIL)
       IF(IFAIL.NE.0)THEN
            PRINT *,' !!!!!! DLCTRW WARNING : Opening ',FILE(1:NCFILE),
     -              ' failed ; the tracks data will not be written.'
            RETURN
       ENDIF
       CALL DSNLOG(FILE,'Track data','Sequential','Write     ')
       IF(LDEBUG)PRINT *,' ++++++ DLCTRW DEBUG   : Dataset ',
     -      FILE(1:NCFILE),' opened on unit 12 for seq write.'
*   Now write a heading record to the file.
       CALL DATTIM(DATE,TIME)
       WRITE(STRING,'(''% Created '',A8,'' At '',A8,1X,A8,'' TRACK   '',
     -      1X,''"'',A29,''"'')') DATE,TIME,MEMBER,REMARK
       WRITE(12,'(A80)',IOSTAT=IOS,ERR=2010) STRING(1:80)
       IF(LDEBUG)THEN
            PRINT *,' ++++++ DLCTRW DEBUG   : Dataset heading record:'
            PRINT *,STRING(1:80)
       ENDIF
*   Write some cell information to the dataset for future checks.
       WRITE(12,'('' Some cell data for checks follows'')',IOSTAT=IOS,
     -      ERR=2010)
       WRITE(12,'('' CELLID: '',A)',IOSTAT=IOS,ERR=2010) CELLID
       WRITE(12,'('' NWIRE:  '',I10,'' TYPE: '',A3,I2,
     -      '' POLAR: '',L1,'' TUBE: '',L1)',
     -      IOSTAT=IOS,ERR=2010) NWIRE,TYPE,ICTYPE,POLAR,TUBE
*   Write some gas information to the dataset for future checks.
       WRITE(12,'('' Some gas data for checks follows'')',IOSTAT=IOS,
     -      ERR=2010)
       WRITE(12,'('' GASID : '',A)',IOSTAT=IOS,ERR=2010) GASID
       WRITE(12,'('' NGAS: '',I10,'' GASOK: '',8L1)',IOSTAT=IOS,
     -      ERR=2010) NGAS,(GASOK(I),I=1,8)
*   Write the track to the dataset.
       WRITE(12,'('' TRACK: '',6E15.8)',IOSTAT=IOS,ERR=2010)
     -      XT0,YT0,ZT0,XT1,YT1,ZT1
       WRITE(12,'('' Principal direction: '',I3)',IOSTAT=IOS,ERR=2010)
     -      ITRMAJ
*   And write the drifting information to the dataset.
       WRITE(12,'('' Drifting information follows: '')')
       WRITE(12,'('' TRAFLG: '',9L1,'' NTRBNK: '',I10)',IOSTAT=IOS,
     -      ERR=2010) (TRAFLG(J),J=1,9),NTRBNK
       WRITE(12,'(''          x [cm]         y [cm]         z [cm]''/
     -      '' time [microsec]   s [microsec] multiplication'',
     -      ''     attachment         vector    status'',
     -      '' approach angle'')',IOSTAT=IOS,ERR=2010)
       DO 210 I=1,NTRBNK
       WRITE(12,'(1X,3E15.8/1X,5E15.8,I10,E15.8)',IOSTAT=IOS,ERR=2010)
     -      (TRABNK(I,J),J=1,7),TRAVEC(I),NINT(TRABNK(I,8)),TRABNK(I,9)
210    CONTINUE
*** Close the file after the operation.
       CLOSE(UNIT=12,STATUS='KEEP',IOSTAT=IOS,ERR=2030)
       CALL TIMLOG('Writing track information to a dataset: ')
       RETURN
