2010   CONTINUE
       PRINT *,' ###### DLCTRW ERROR   : I/O error while writing to '//
     -      FILE(1:NCFILE)//' via unit 12 ; no track data written.'
       CALL INPIOS(IOS)
       CLOSE(UNIT=12,STATUS='KEEP',IOSTAT=IOS,ERR=2030)
       RETURN
