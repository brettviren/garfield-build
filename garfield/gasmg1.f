*-----------------------------------------------------------------------
*   GASMG2 - Called by the gas mixing routines for integrations. Breaks
*            the integration up into steps without discontinuitites so
*            that Gaussian integration by DGMLT works.
*   (Last changed on 28/ 9/92.)
*-----------------------------------------------------------------------
       implicit none
       INTEGER MXWIRE,MXSW,MXLIST,MXCHA,MXGRID,MXMATT,MXPOLE,MX3D,
     -         MXPSTR,
     -         MXPAIR,MXPART,MXFOUR,MXCLUS,
     -         MXLINE,MXEQUT,
