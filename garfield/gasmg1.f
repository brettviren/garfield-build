       PARAMETER (MXSBUF= 20000)
       PARAMETER (MXMCA = 50000)
*   The parameter MXNBMC must equal MXGNAM (sequence MAGBPARM) !
       INTEGER MXNBMC
       PARAMETER(MXNBMC=60)
*-----------------------------------------------------------------------
*   GMXDAT - Common block for gas mixing.
*   (Last changed on 20/ 2/97.)
*-----------------------------------------------------------------------
       REAL BREAK,FRAC,XLOSCH,EFLD,ESTEP,ECRIT
       INTEGER NBREAK
       COMMON /GMXDAT/ BREAK(MXLIST),FRAC(MXFRAC),XLOSCH,
     -      EFLD,ESTEP,ECRIT,NBREAK
       DOUBLE PRECISION A,B,END,X(*),DGMLT1
       INTEGER I,NPOINT
       EXTERNAL F,DGMLT1
*** Initial value.
       GASMG1=0.0D0
       DO 10 I=1,MAX(1,NBREAK)
       IF(BREAK(I).GE.END)RETURN
       A=BREAK(I)
       IF(I.GE.NBREAK)THEN
            B=END
       ELSE
            B=MIN(END,DBLE(BREAK(I+1)))
       ENDIF
       NPOINT=MAX(1,NINT((B-A)/ESTEP))
       GASMG1=GASMG1+DGMLT1(F,A,B,NPOINT,6,X)
10     CONTINUE
       END
