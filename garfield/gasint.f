       VGAS2(I,J,K)=0.0
       XGAS2(I,J,K)=0.0
       YGAS2(I,J,K)=0.0
       DGAS2(I,J,K)=0.0
       OGAS2(I,J,K)=0.0
       AGAS2(I,J,K)=0.0
       AORIG2(I,J,K)=0.0
       BGAS2(I,J,K)=0.0
       HGAS2(I,J,K)=0.0
       MGAS2(I,J,K)=0.0
       WGAS2(I,J,K)=0.0
30     CONTINUE
40     CONTINUE
20     CONTINUE
