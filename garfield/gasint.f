       JVEXTR=0
       JXEXTR=0
       JYEXTR=0
       JDEXTR=0
       JOEXTR=0
       JAEXTR=0
       JBEXTR=0
       JHEXTR=0
       JMEXTR=0
       JWEXTR=0
