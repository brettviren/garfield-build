       DO 250 L=1,6
       DO 240 K=1,MXBTAB
       DO 230 J=1,MXBANG
       DO 220 I=1,MXLIST
       SGAS2(I,J,K,L)=0
220    CONTINUE
230    CONTINUE
240    CONTINUE
250    CONTINUE
       DO 350 L=1,MXEXG
       DO 340 K=1,MXBTAB
       DO 330 J=1,MXBANG
       DO 320 I=1,MXLIST
       EXGAS2(I,J,K,L)=0
320    CONTINUE
330    CONTINUE
340    CONTINUE
350    CONTINUE
       DO 450 L=1,MXIOG
       DO 440 K=1,MXBTAB
       DO 430 J=1,MXBANG
       DO 420 I=1,MXLIST
       IOGAS2(I,J,K,L)=0
420    CONTINUE
430    CONTINUE
440    CONTINUE
450    CONTINUE
