       REAL FUNCTION GASLOR(EX,EY,EZ,BX,BY,BZ)
*-----------------------------------------------------------------------
*   GASLOR - Function calculating the Lorentz angle in a gas.
*   (Last changed on 27/ 9/07.)
*-----------------------------------------------------------------------
       implicit none
       INTEGER MXWIRE,MXSW,MXLIST,MXCHA,MXGRID,MXMATT,MXPOLE,MX3D,
     -         MXPSTR,
     -         MXPAIR,MXPART,MXFOUR,MXCLUS,
     -         MXLINE,MXEQUT,
