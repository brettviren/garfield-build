     -      AEXTR1,AEXTR2,AEXTR3,AEXTR4,
     -      BEXTR1,BEXTR2,BEXTR3,BEXTR4,
     -      HEXTR1,HEXTR2,HEXTR3,HEXTR4,
     -      MEXTR1,MEXTR2,MEXTR3,MEXTR4,
     -      WEXTR1,WEXTR2,WEXTR3,WEXTR4,
     -      OEXTR1,OEXTR2,OEXTR3,OEXTR4,
     -      SEXTR1(6),SEXTR2(6),SEXTR3(6),SEXTR4(6),
     -      EEXTR1(MXEXG),EEXTR2(MXEXG),EEXTR3(MXEXG),EEXTR4(MXEXG),
     -      ZEXTR1(MXIOG),ZEXTR2(MXIOG),ZEXTR3(MXIOG),ZEXTR4(MXIOG),
     -      IVMETH,IXMETH,IYMETH,IDMETH,IAMETH,IBMETH,IHMETH,IMMETH,
     -      IWMETH,IOMETH,ISMETH,IEMETH,IZMETH,
     -      IVEXTR,IXEXTR,IYEXTR,IDEXTR,IAEXTR,IBEXTR,IHEXTR,IMEXTR,
     -      IWEXTR,IOEXTR,ISEXTR,IEEXTR,IZEXTR,
     -      JVEXTR,JXEXTR,JYEXTR,JDEXTR,JAEXTR,JBEXTR,JHEXTR,JMEXTR,
     -      JWEXTR,JOEXTR,JSEXTR,JEEXTR,JZEXTR,
     -      NGAS,NCLS,NBANG,NBTAB,NFTAB,NFCLS,
     -      IATHR,IBTHR,IHTHR,
     -      NEXGAS,NIOGAS,NCSGAS,ICSTYP(MXCSG),
     -      GASOK(20),GASOPT(20,4),
     -      TAB2D,HEEDOK,SRIMOK,TRIMOK,GASSET
       COMMON /GASCHR/ FCNTAB,FCNCLS,CLSTYP,GASID,DSCEXG,DSCIOG,DSCCSG
       LOGICAL MAGOK
       REAL ALFA,B0X,B0Y,B0Z,SUSWIR,SUSGAS,BSCALE,BFMIN,BFMAX,
     -      BFXMIN,BFYMIN,BFZMIN,BFXMAX,BFYMAX,BFZMAX
       INTEGER MAGSRC,
     -      IBXTYP,IBYTYP,IBZTYP,
     -      IRB0X,IRB0Y,IRB0Z,IRV0X,IRV0Y,IRV0Z,
     -      IENB0X,IENB0Y,IENB0Z,IBXDIR,IBYDIR,IBZDIR,
     -      NCB0X,NCB0Y,NCB0Z
       CHARACTER*(MXCHAR) FUNB0X,FUNB0Y,FUNB0Z
       COMMON /MAGDAT/ ALFA,SUSWIR,SUSGAS,
     -      B0X,B0Y,B0Z,BSCALE,BFMIN,BFMAX,
     -      BFXMIN,BFYMIN,BFZMIN,BFXMAX,BFYMAX,BFZMAX,
     -      MAGSRC,IBXTYP,IBYTYP,IBZTYP,
     -      IRB0X,IRB0Y,IRB0Z,IRV0X,IRV0Y,IRV0Z,
     -      IENB0X,IENB0Y,IENB0Z,IBXDIR,IBYDIR,IBZDIR,
     -      NCB0X,NCB0Y,NCB0Z,
     -      MAGOK
       COMMON /MAGCHR/ FUNB0X,FUNB0Y,FUNB0Z
       REAL EX,EY,EZ,BX,BY,BZ,B,E,EBANG,DIVDIF,AUXLOR
       INTEGER IFAIL
       EXTERNAL DIVDIF
*** Return 0 if the data is missing.
       IF(.NOT.GASOK(7))THEN
            GASLOR=0.0
            RETURN
       ENDIF
*** Obtain the magnitude of the electric field.
       E=SQRT(EX**2+EY**2+EZ**2)
       IF(E.LE.0.0)THEN
            GASLOR=0.0
            RETURN
       ENDIF
*** Treat the case that the table is 2-dimensional.
       IF(TAB2D)THEN
*   B field magnitude.
            B=SQRT(BX**2+BY**2+BZ**2)
*   Obtain the angle between B field and E field.
            IF(E*B.EQ.0)THEN
                 EBANG=BANG(1)
            ELSEIF(ABS(EX*BX+EY*BY+EZ*BZ).GT.0.2*E*B)THEN
                 EBANG=ASIN(MIN(1.0,SQRT((EX*BY-EY*BX)**2+
     -                (EX*BZ-EZ*BX)**2+(EZ*BY-EY*BZ)**2)/(E*B)))
            ELSE
                 EBANG=ACOS(MIN(1.0,ABS((EX*BX+EY*BY+EZ*BZ)/(E*B))))
            ENDIF
*   Interpolate.
            CALL BOXIN3(WGAS2,EGAS,BANG,BTAB,MXLIST,MXBANG,MXBTAB,
     -           NGAS,NBANG,NBTAB,E/PGAS,EBANG,B,AUXLOR,IWMETH,IFAIL)
            GASLOR=AUXLOR
*   Verify error.
            IF(IFAIL.NE.0)GASLOR=0
*** Treat the case that the table is 1-dimensional.
       ELSE
*   Only one point.
            IF(NGAS.LE.1)THEN
                 GASLOR=WGAS(1)
*   Extrapolation towards small E/p.
            ELSEIF(E/PGAS.LT.EGAS(1))THEN
                 GASLOR=WGAS(1)
                 IF(JWEXTR.EQ.1)GASLOR=WEXTR3+WEXTR4*E/PGAS
                 IF(JWEXTR.EQ.2)GASLOR=EXP(MIN(50.0,
     -                WEXTR3+WEXTR4*E/PGAS))
*   Extrapolation towards large E/p.
            ELSEIF(E/PGAS.GT.EGAS(NGAS))THEN
                 GASLOR=WGAS(NGAS)
                 IF(IWEXTR.EQ.1)GASLOR=WEXTR1+WEXTR2*E/PGAS
                 IF(IWEXTR.EQ.2)GASLOR=EXP(MIN(50.0,
     -                WEXTR1+WEXTR2*E/PGAS))
*   Intermediate points, spline interpolation.
            ELSEIF(IWMETH.EQ.0)THEN
                 CALL INTERP(EGAS,WGAS,CWGAS,NGAS,E/PGAS,AUXLOR,IFAIL)
                 GASLOR=AUXLOR
*   Intermediate points, Newton interpolation.
            ELSE
                 GASLOR=DIVDIF(WGAS,EGAS,NGAS,E/PGAS,IWMETH)
            ENDIF
       ENDIF
       END
