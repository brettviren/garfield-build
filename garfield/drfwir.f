       ENDIF
*   Start a drift line at enough distance from the wire.
       IF(Q*E(I).LT.0)THEN
            RDIST=0.51*RTRAP*D(I)
       ELSE
            RDIST=0.51*D(I)
       ENDIF
       CALL DLCALC(X(I)+RDIST*COS(ANGLE),Y(I)+RDIST*SIN(ANGLE),
     -      0.0,Q,ITYPE)
*** Print data on this drift line if requested.
       IF(LLINPR)THEN
            CALL DLCSTF(ISTAT,STASTR,NCSTAT)
            WRITE(LUNOUT,'(1X,F10.2,I10,I10,2X,E15.8,2X,A)')
     -           180*ANGLE/PI,I,NU,TU(NU),STASTR(1:NCSTAT)
       ENDIF
*** Plot the drift line obtained.
       IF(LLINPL)CALL DLCPLT
*** Invert TU in order to obtain the time distance from the sense wire.
       IF(LEQREV)THEN
            DO 80 J=1,NU
            TU(J)=TU(NU)-TU(J)
80          CONTINUE
*** Reverse XU,YU and TU so that they can be treated as plot vectors.
            DO 90 J=1,INT(NU/2.0)
            TRANSF=TU(J)
            TU(J)=TU(NU-J+1)
            TU(NU-J+1)=TRANSF
            TRANSF=XU(J)
            XU(J)=XU(NU-J+1)
            XU(NU-J+1)=TRANSF
            TRANSF=YU(J)
            YU(J)=YU(NU-J+1)
            YU(NU-J+1)=TRANSF
90          CONTINUE
*** Don't accept lines not leading to a wire.
            IF((ISTAT.GE.-15.AND.ISTAT.LE.-11).OR.
     -           (ISTAT.GE.1.AND.ISTAT.LE.NWIRE).OR.
     -           (ISTAT.GE.2*MXWIRE+1.AND.ISTAT.LE.2*MXWIRE+NSOLID))
     -      CALL DRFEQT(TSTEP,ISTAT)
       ELSE
            CALL DRFEQT(TSTEP,I)
       ENDIF
20     CONTINUE
10     CONTINUE
*** Register the amount of CPU time used for these steps.
       CALL TIMLOG('Making a wire drift-line plot:          ')
*** Plot the equal time contours.
       IF(LEQTPL)CALL DRFEQP
*** End this page.
       IF(LEQTPL.OR.LLINPL)CALL GRNEXT
*** And print any error messages accumulated by DRFEQ.
       IF(LEQTPL)CALL DRFEQE
       END
